import random

PUNCTUATION_MAP = {
    '.': [' نقطه '],
    ':': [' دو نقطه '],
    '،': [' ویرگول ', ' کاما '],
    '_': [' آندرلاین ', ' زیرخط '],
    '-': [' خط تیره ', ' خط فاصله '],
    '/': [' ممیز '],
    '٪': [' درصد '],
    '%': [' درصد '],
    '؛': [' نقطه ویرگول '],
    '؟': [' علامت سؤال '],
    '?': [' علامت سؤال '],
    '!': [' علامت تعجب '],
    '(': [' پرانتز باز '],
    ')': [' پرانتز بسته '],
    '[': [' کروشه باز ', ' آکولاد باز ', ' قلاب باز '],
    ']': [' کروشه بسته ', ' آکولاد بسته ', ' قلاب بسته '],
    '«': [' گیومه باز '],
    '»': [' گیومه بسته '],
    '…': [' سه نقطه '],
    '@': [' علامت اتساین '],
    '#': [' علامت شارپ ', ' علامت هشتگ '],
    '&': [' و '],
    '*': [' علامت ستاره '],
    '+': [' جمع '],
    '=': [' مساوی '],
    '|': [' یا '],
    '<': [' بزرگتر '],
    '>': [' کوچکتر '],
    '\'': [' کوتیشن '],
    '"': [' دابل کوتیشن ']
}

NEW_LINES = ['سر خط', 'خط جدید', 'سر سطر', 'پاراگراف جدید']


def punctuation_map(
        char: str,
        mode: str = 'TTS'
) -> str:
    if char == '\n':
        if mode == 'TTS':
            rand = random.randint(0, 3)
            return NEW_LINES[rand]
        else:
            pass
    else:
        if mode == 'TTS':
            try:
                return PUNCTUATION_MAP[char][0]
            except KeyError:
                return char
        else:
            pass
